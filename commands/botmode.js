// ==================== commands/botmode.js ====================
import fs from "fs";
import path from "path";
import { contextInfo } from "../utils/contextInfo.js";
import config, { saveConfig } from "../config.js";

const filePath = path.join(process.cwd(), "data/botMode.json");

// Cr√©e le fichier JSON s'il n'existe pas
if (!fs.existsSync(filePath)) {
  fs.writeFileSync(filePath, JSON.stringify({ mode: "public" }, null, 2));
}

// Fonction pour lire le JSON
function loadBotMode() {
  try {
    const data = JSON.parse(fs.readFileSync(filePath, "utf-8"));
    return data.mode || "public";
  } catch {
    return "public";
  }
}

// Fonction pour sauvegarder le JSON
function saveBotMode(mode) {
  fs.writeFileSync(filePath, JSON.stringify({ mode }, null, 2));
  global.botMode = mode;
  console.log("ü§ñ Mode Bot :", mode);
}

// Initialise global.botMode
global.botMode = loadBotMode();

export default {
  name: "botmode",
  description: "‚öôÔ∏è Configure le mode du bot (public/priv√©)",
  category: "Owner",
  ownerOnly: true, // ‚úÖ handler bloque d√©j√† les non-owners

  run: async (kaya, m, msg, store, args, context) => {
    try {
      if (!context.isOwner) {
        return kaya.sendMessage(
          m.chat,
          { text: "üö´ Cette commande est r√©serv√©e au propri√©taire du bot.", contextInfo },
          { quoted: m }
        );
      }

      // Si aucune option ‚Üí on affiche l'aide
      if (!args[0]) {
        return kaya.sendMessage(
          m.chat,
          {
            text: `‚öôÔ∏è Utilisation du mode bot :\n\n- *.public on* ‚Üí Active le mode public\n- *.private on* ‚Üí Active le mode priv√©\n\nüìå Mode actuel : *${global.botMode.toUpperCase()}*`,
            contextInfo
          },
          { quoted: m }
        );
      }

      const action = args[0].toLowerCase();
      const command = m.body.split(" ")[0].replace(config.PREFIX, "").toLowerCase();

      if (command === "public" && action === "on") {
        saveBotMode("public");
        if (saveConfig) saveConfig({ botMode: "public" });
        return kaya.sendMessage(
          m.chat,
          { text: "‚úÖ Mode bot chang√© : *PUBLIC*", contextInfo },
          { quoted: m }
        );
      }

      if ((command === "private" || command === "priv√©") && action === "on") {
        saveBotMode("priv√©");
        if (saveConfig) saveConfig({ botMode: "priv√©" });
        return kaya.sendMessage(
          m.chat,
          { text: "‚úÖ Mode bot chang√© : *PRIV√â*", contextInfo },
          { quoted: m }
        );
      }

      return kaya.sendMessage(
        m.chat,
        {
          text: "‚ùå Mauvaise utilisation.\n\n‚öôÔ∏è Utilisation correcte :\n- *.public on*\n- *.private on*",
          contextInfo
        },
        { quoted: m }
      );

    } catch (err) {
      console.error("‚ùå Erreur botmode.js:", err);
      return kaya.sendMessage(
        m.chat,
        { text: "‚ùå Une erreur est survenue lors du changement de mode bot.", contextInfo },
        { quoted: m }
      );
    }
  }
};